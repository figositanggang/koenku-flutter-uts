bekas penanaman tidak nampak cekung atau cembung.
E. Penanaman Tanaman Pelindung
Ada 2 macam tanaman pelindung : tanaman pelindung
sementara  dan  tetap.  Tanaman  pelindung  sementara
dipakai  jenis Crotalaria sp  dan Tephrosia sp.  Tanaman
bersifat ganda karena menambah kesuburan tanah dimana
bintil  akar  dapat  mengikat  unsur  hara  N.  Setelah  tanaman
teh  berumur  2-3  tahun  sebaiknya  sudah  ada  pohon
pelindung  tetap  yang  ditanam  setahun  sebelum  teh
ditanam  atau  bersamaan. Jenis  pohon  pelindung  yang
dianjurkan  : Albizia  falcata,  Albizia  sumatrana,  Albizia chinensis,  Albizia  procera,  Derris  microphylla,  Leucaena
glauca,  Leucaena  pulverulenta (Gambar  7),  Erythrina
subumbrans,  Erythrina  poeppingiana,  Gliricidia  maculata,
Acacia  decurens,  Media  azedarach dan ,  Grevillea  robusta

5. Pengelolaan Tanaman
Untuk  mencapai  tujuan pengelolaan  tanaman yang
baik,  maka  kegiatan  yang  harus  dilakukan  terdiri  atas  :
penyiangan,  pembuatan  rorak,  penyulaman,  pengelolaan
tanaman pelindung, dan pembentukan bidang petik.
A. Penyiangan
Apabila  penanaman  tanaman  teh  telah  selesai
dilakukan,  tanah  perlu  diratakan  kembali. Satu  setengah
atau 2 bulan setelah tanaman ditanam, gulma mulai tumbuh dan perlu disiangi. Penyiangan dapat juga dilakukan dengan
herbisida  bila  tersedia.  Penyiangan  dengan  cara  manual
perlu diulangi 1,5 – 2 bulan kecuali ada gangguan serangga
hama/penyakit  penyiangan  dilakukan  dengan  cara  strip
weeding.
B. Pembuatan Rorak
Sesuai  dengan  kemiringan  tanah  rorak  dibuat  2 – 3
baris tanaman secara selang seling dengan ukuran panjang
200 cm, lebar 40 cm dan dalam 60 cm. Rorak perlu dikuras 3
kali  dalam  setahun.  Tanah  yang  menutup  dikeluarkan  dari
rorak  agar  berfungsi  kembali.  Fungsi  dari  pada  rorak  ini
sebagai  kantong  peresapan  air  yang  berguna  dimusim
kering.  Rorak  disamping  mencegah  erosi  dapat
memperbaiki abrasi tanah dan tempat penampungan bahan
organik. Jumlah  rorak  di  daerah  datar  jumlahnya  dapat
sama atau lebih dibanding lereng yang miring, tergantung
aliran air.
C. Penyulaman
Penyulaman tanaman yang mati harus diganti dengan
yang  baru.  Bibit  untuk  menyulam  adalah  bibit  terbaik  dari
klon yang sama. Penyulaman dilakukan mulai 2 – 4 minggu
setelah  adanya  penanaman.  Penyulaman  harus  dilakukan
sampai  tanaman  berumur  2 tahun.  Penyulaman  tahun
pertama  diperkirakan  sekitar  10%,  tahun  ke  2  sebesar  5%
sehingga tanaman menghasilkan populasi menjadi penuh.
D. Pengelolaan Pohon Pelindung
Jenis pohon pelindung yang berfungsi ganda sebagai
pelindung  dan  penghasil  hara  nitrogen seperti  : Crotalaria
usaramoensis, C. anaggreoides dan Tephrosia sp. dapat
ditanam selang dua baris di antara tanaman teh. Bila tinggi
tanaman  telah  lebih  1  m,  tanaman  ini  perlu  dipangkas  50
cm  karena  akar  mengganggu  tanaman  teh.  Pada  jarak
setiap  1  m  dibiarkan  1 – 2  pohon  pupuk  hijau  ini  sebagai
pohon  pelindung  sementara.  Pemangkasan  tanaman
pelindung ini pada dua musim hujan dipangkas 4 – 6 bulan
sekali  dan  sisa  pangkasan  dijadikan  mulsa  tanaman  teh.
Pemberian mulsa untuk tanaman ternyata tidak akan cukup
sehingga  diperlukan  mulsa  dari  tanaman  seperti  :  rumput
Guatemala,  rumput-rumputan  dan  jerami.  Penanaman
pohon pelindung tetap sebaiknya ditanam 1 tahun sebelum
atau  bersamaan  waktu  tanaman  teh  ditanam.  Pohon
pelindung yang mati agar segera disulam.
E. Pembentukan Bidang Petik
Pembentukan  bidang  petik  berfungsi  agar  tanaman
menjadi  bentuk  perdu,  dimana  kerangka  tanaman
percabangannya  ideal  dengan bidang  petik  yang  luas
sehingga  pucuk  yang  dihasilkan  banyak.  Ada  tiga  cara
membentuk  bidang  petik  :  1)  cara  pemangkasan  dan
pemenggalan, 2) cara perundukan, dan 3) cara kombinasi.
 Cara pemangkasan dan pemenggalan.
1. Cara pemangkasan dilakukan pada bahan tanaman asal
biji umur 2 tahun dipangkas setinggi 10 – 15 cm. Setelah
tanaman dilapangan 1 – 1,5 tahun dipangkas setinggi 30
cm, setelah 2,5 tahun dipangkas selektif dahan setinggi 45 cm dan tiga sampai empat bulan kemudian dilakukan
jendangan 60 – 65 cm dari permukaan tanah.
2. Cara  pemenggalan  dilakukan  pada  tanaman  asal  setek
atau biji dalam polibag. Setelah bibit dilapangan umur 4
– 6 bulan, batang utama dipenggal setinggi 15 – 20 cm
dengan meninggalkan minimal 5 lembar daun (Gambar
9). Kemudian setelah cabang baru muncul setinggi 50 –
60  cm,  kira-kira  6 – 9  bulan  setelah  batang  utama
dipenggal  terdapat  cabang  yang  tumbuh  kuat  keatas
dipotong  pada  ketinggian  30  cm  untuk  memacu
pertumbuhan  kesamping.  Tiga  sampai  enam  bulan
kemudian  pada  percabangan  baru  tinggi  60 – 70  cm
dipangkas  selektif  setinggi  45  cm.  Tunas  yang  tumbuh
dibiarkan  sampai  3-6 bulan,  kemudian  dijendang pada
ketinggian 60 – 65 cm.
 Cara Perundukan
Perundukan  adalah  suatu  cara  membentuk  bidang
petik  dengan  melengkungkan  batang  utama  dan  cabang-
cabang  sekunder.  Cara  ini  dilakukan  agar  bahan makanan terakumu-lasi ke bagian  sisi  atas  dari  batang.  Cara
pelaksanaan dilakukan sebagai berikut :
 Setelah bibit di lapangan 6 bulan, batang dilengkungkan
membentuk 45  dan pucuk peko dipotong (Gambar 10).
 Sesudah  6  bulan  dilengkungkan,  tunas  sekunder  yang
telah  mencapai  panjang  50  cm  dilakukan  perundukan.
Hal  ini  dilakukan  sampai  beberapa  kali  sehingga
menutup ke segala arah.
 Cabang  yang  masih  tumbuh  ke atas,  dipotong  30  cm.
Tunas  yang  tumbuh  setelah  perundukan  kedua
dibiarkan  sampai ketinggian 70  cm kemudian
dipotong setinggi 45 cm.
 Dua sampai tiga bulan sesudah itu pucuk yang tumbuh
dijendang pada ketinggian 60 cm atau 20 cm di bidang
pangkas.
Cara kombinasi  pemangkasan,  pemenggalan  dan
perundukan  diawali  dengan  pemangkasan  batang  utama
kemudian  perundukan. Maksud  dari  cara  kombinasi
pemangkasan  dan  perundukan  agar  dapat  mengurangi
kerugian  yang  ditimbulkan  dari  kedua  sistem  ini.  Cara  ini
dapat juga dilakukan pada tanaman asal biji yang ditanam
berupa stum.
Pelaksanaan cara ini sebagai berikut :
 Setalah umur tanaman 6 bulan batang utama dipotong
pada ketinggian 20 cm dengan meninggalkan minimal 5
lembar daun.
 Tunas sekunder yang tumbuh setelah 6 bulan dibiarkan
mencapai  panjang  50  cm  kemudian  dirundukkan
kesegala arah.
 Setelah 6 bulan dirundukkan, tunas daun yang tumbuh
60 – 70  cm  dilakukan  pemotongan  setinggi  45  cm.
Jendangan  setinggi  60 – 65  cm  dilakukan  3  bulan
setelah dipotong di atas.
Menurut hasil penelitian PPTK Gambung, keuntungan
cara centering-bending selain  mudah  dilakukan,  kerangka
perdu  telah  terbentuk sebelumnya dan tingkat  kesalahan
